def logo_display():
    def clear():
        print("\033[2J\033[H", end="")

    def draw(brightness, dots=None):
        clear()
        
        print(f"\033[38;5;{brightness}m{CUP}\033[0m")
        
        if dots is not None:
            print(f"\nLOADING{'.' * dots}")

    frames = list(range(232, 256)) + list(range(255, 231, -1))

    for i, b in enumerate(frames):
        dots = i % 4
        draw(b, dots)
        time.sleep(0.05)

    draw(255)